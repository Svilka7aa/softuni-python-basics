objekt = input()

if objekt == "banana" \
        or objekt == "apple" \
        or objekt == "kiwi" \
        or objekt == "lemon" \
        or objekt == "grapes" \
        or objekt == "cherry":
    print("fruit")
elif objekt == "tomato" or objekt == "cucumber" or objekt == "pepper" or objekt == "carrot":
    print("vegetable")
else:
    print("unknown")
